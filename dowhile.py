def main():                     # def means define and main is a function
    i = 0                       # i is a variable and 0 is a value
    while i<12:                 # while loop
        print(i, end ="  ")     # print and end
        i += 1                  # increment by 1

        if i >= 12:             # if loop
            break               # break

main()                          # call
         